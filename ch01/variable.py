#정수형
i=70
print(i)
print(type(i)) #int

#실수형
d=3.14
print(d)
print(type(d)) #float

#논리형
b1=True
b2=False
print(b1)
print(b2)
print(type(b1)) #bool
print(type(b2)) #bool
#문자
str='Python'
print(str)
print(type(str)) #str

#리스트
list=[10,20,40]
print(list)
print(type(list))# <class 'list'>

#튜플
t=(10,20,30)
print(t)
print(type(t)) #<class 'tuple'>

#딕셔너리 : 키-값 쌍으로 된 해시테이블 구조
d={'a':1, 'b':'2', 'c':3}
print(d)
print(type(d)) #<class 'dict'>